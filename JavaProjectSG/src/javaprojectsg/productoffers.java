    public ArrayList<Offer> 
        listoffers(String ValtoSearch){
        ArrayList<Offer> offersList=new ArrayList<Offer> ();
        
        Statement st;
        ResultSet rs;
        try{
            Connection con= getConnection();
            st=con.createStatement();
            String query="select * from offers where cgryname='"+ValtoSearch+"'";
            
            rs=st.executeQuery(query);
            Offer pr;
            if(rs.next()==false)
            {
                JOptionPane.showMessageDialog(null,"Offer not available");
            }
            while(rs.next()){
                pr=new Offer(
                rs.getString("cgryname"), rs.getDouble("discountpct"), rs.getString("site"));
              
             offersList.add(pr);   
                   
            }
        }
        catch(Exception ex){
             System.out.println(ex.getMessage());
        }
        return offersList;
    }   
 public void findOffers(){
        ArrayList<Offer> prds=listoffers(jTextField1.getText());
        DefaultTableModel model=new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"cgryname","discountpct","site"});
        Object[] row=new Object[3];
        for(int i=0;i<prds.size(); i++){
            row[0]=prds.get(i).getcgryname();
            row[1]=prds.get(i).getdiscountpct();
            row[2]=prds.get(i).getsite();
            model.addRow(row);
        }
        jTable1.setModel(model);
    }
